// in this version we return a slice string, so when we iterate, we get the literal string on it
//and not the position where the '_' is
fn first_word(s: &str) -> &str {
    let bytes = s.as_bytes();

    for (i, &item) in bytes.iter().enumerate() {
        if item == b' ' {
            return &s[0..i];
        }
    }

    &s[..]
}
